export function applyTemplateVariables(sections, variables) {
  return sections.map((s) => {
    let content = s.content;
    Object.entries(variables).forEach(([key, value]) => {
      content = content.replaceAll(`{${key}}`, value || `[${key}]`);
    });
    return { ...s, content };
  });
}
